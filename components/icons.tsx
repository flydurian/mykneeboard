// 항공사 로고 컴포넌트
export const AirlineLogo: React.FC<IconProps & { airline: string }> = ({ airline, className }) => {
    const [imageError, setImageError] = React.useState(false);

    if (airline === 'OZ') {
        if (imageError) {
            return (
                <span className={`${className} bg-pink-500 text-white text-xs font-bold rounded px-1`}>
                    OZ
                </span>
            );
        }
        return (
            <img
                src="/airline-logos/oz-logo.png"
                alt="아시아나항공 로고"
                className={className}
                style={{
                    width: '24px',
                    height: '24px',
                    backgroundColor: 'transparent',
                    background: 'transparent',
                    objectFit: 'contain'
                }}
                onError={() => setImageError(true)}
                onLoad={() => setImageError(false)}
            />
        );
    } else if (airline === 'KE') {
        if (imageError) {
            return (
                <span className={`${className} bg-blue-600 text-white text-xs font-bold rounded px-1`}>
                    KE
                </span>
            );
        }
        return (
            <img
                src="/airline-logos/ke-logo.png"
                alt="대한항공 로고"
                className={className}
                style={{
                    width: '24px',
                    height: '24px',
                    backgroundColor: 'transparent',
                    background: 'transparent',
                    objectFit: 'contain'
                }}
                onError={() => setImageError(true)}
                onLoad={() => setImageError(false)}
            />
        );
    } else if (airline === '7C') {
        if (imageError) {
            return (
                <span className={`${className} bg-orange-500 text-white text-xs font-bold rounded px-1`}>
                    7C
                </span>
            );
        }
        return (
            <img
                src="/airline-logos/jeju-long.png"
                alt="제주항공 로고"
                className={className}
                style={{
                    width: '24px',
                    height: '24px',
                    backgroundColor: 'transparent',
                    background: 'transparent',
                    objectFit: 'contain',
                    maxWidth: '24px',
                    maxHeight: '24px'
                }}
                onError={() => setImageError(true)}
                onLoad={() => setImageError(false)}
            />
        );
    }
    return null;
};

